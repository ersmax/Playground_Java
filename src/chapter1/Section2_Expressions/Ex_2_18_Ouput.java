/*
What is the output of the following program lines?
double number = (1/3) * 3;
System.out.println("(1/3) * 3 is equal to " + number);
 */


package chapter1.Section2_Expressions;

public class Ex_2_18_Ouput {
    public static void main(String[] args) {
        double number = (1/3) * 3;  // Output: (1/3) * 3 is equal to 0.0
        System.out.println("(1/3) * 3 is equal to " + number);
    }
}
